#include "../base/base.h"
#include "../base/config.h"

int main(int argc, char *argv[])
{
   init_boot_timestamp();
   log_init("RubyTimeInit");
   long long t = 0;
   FILE* fd = fopen(FILE_BOOT_TIMESTAMP, "r");
   if ( NULL != fd )
   {
      fscanf(fd, "%lld\n", &t);
      fclose(fd);
      log_line("Initial timestamp: %lld", t);
   }
   else
      log_line("Failed to read initial timestamp.");
   log_line("TimeInit done.");
   return (0);
} 